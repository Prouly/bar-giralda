    private void jButtonDividirCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDividirCuentaActionPerformed
        double dividirEntre = Double.parseDouble(jTextFieldDividirEntre.getText());      

        if (dividirEntre !=0) {
            double totalDividido = comanda.getImporteTotal()/dividirEntre;
            DecimalFormat df = new DecimalFormat("0.00");
            String totalDivididoFormat = df.format(totalDividido);
            
            jTextFieldImporteCuentaDividido.setText(totalDivididoFormat+" €");
        } else {
            jTextFieldImporteCuentaDividido.setFont(new java.awt.Font("Tahoma", 1, 14));
            jTextFieldImporteCuentaDividido.setText("No se puede dividir entre 0");
        }
        
    }//GEN-LAST:event_jButtonDividirCuentaActionPerformed

