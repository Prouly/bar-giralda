    private void actualizarMostrarPrecioComanda() {
        DecimalFormat df = new DecimalFormat("0.00");
        String numeroFormateado = df.format(listaComandas.get(listaComandas.size()-1).getImporteTotal());
        String precioTotalComanda = "PRECIO TOTAL: " + numeroFormateado + " €";
        this.jLabelPrecioTotal.setText(precioTotalComanda);
        jTextFieldImporteCuenta.setText(precioTotalComanda);
        jTextFieldImporteCuentaCobrar.setText(precioTotalComanda);

        // Actualizar el modelo de tabla con la comanda actual
        ComandaTableModel modeloTabla = (ComandaTableModel) tablaComanda.getModel();
        modeloTabla.setComanda(listaComandas.get(listaComandas.size()-1));
        modeloTabla.fireTableDataChanged();
    }
    
    private void mostrarNumerosPantalla(String numero) throws NumberFormatException {
       
        // Agregar el número al final del número decimal actual
        numeroDecimalActual.append(numero);
        
        // Obtener el número decimal completo para mostrarlo
        double numeroDecimalCompleto = Double.parseDouble(numeroDecimalActual.toString());
        
        // Actualizar la representación visual del número en el JLabel
        numeroDecimalFormatUnidades(numeroDecimalCompleto);
    }

