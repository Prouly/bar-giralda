    private void jButtonCobrarEfectivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCobrarEfectivoActionPerformed
        
        String importeEntregadoString = jTextFieldImporteEntregado.getText();
        
        if (importeEntregadoString.matches("[0-9.]+")) {     
            double importeEntregado = Double.parseDouble(jTextFieldImporteEntregado.getText());
            double importeDevolver = importeEntregado - listaComandas.get(listaComandas.size()-1).getImporteTotal();
            boolean isDevolver;


            if (importeDevolver >= 0) {
                isDevolver = true;
            } else {
                isDevolver = false;
            }

            if (isDevolver) {
                listaComandas.get(listaComandas.size()-1).setIsPagado(true);
                
                if (listaComandas.get(listaComandas.size()-1).getMesa().getIdMesa()!=0) {
                    listaComandas.get(listaComandas.size()-1).getMesa().setIsOcupada(false);
                } 
                
                DecimalFormat df = new DecimalFormat("0.00");
                String importeDevolverFormat = df.format(Math.abs(importeDevolver));
                jTextFieldImporteDevolver.setText(importeDevolverFormat);
                // Llamar al método para borrar la comanda completa
                ((ComandaTableModel) tablaComanda.getModel()).borrarComandaCompleta();  
                actualizarMostrarPrecioComanda();
                crearNuevaComanda();

            } else {
 
                DecimalFormat df = new DecimalFormat("0.00");
                String importePendienteFormat = df.format(importeDevolver);
                jTextFieldImporteDevolver.setText("Quedan por pagar: "+importePendienteFormat);
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Debes introducir formato numérico con punto. (Ejemplo 2.40)");
        }
    }//GEN-LAST:event_jButtonCobrarEfectivoActionPerformed

