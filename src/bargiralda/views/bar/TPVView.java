    /**
     * Este método establece un patron de formato para decimales, se los asigna a un numero y se muestra en jLabelPantallaCalculadora
     * @param numero 
     */
    private void numeroDecimalFormatUnidades(double numero) {
        //Formateador de decimales
        DecimalFormat df = new DecimalFormat("0.00");
        String numeroFormateado = df.format(numero);
        
        if (isUnidad) {
            if (numero <=1) {
                jLabelPantallaCalculadora.setText(numeroFormateado+" "+UNIDAD_INICIAL);
            } else {
                jLabelPantallaCalculadora.setText(numeroFormateado+" "+UNIDADES);
            }
        } else {
            //Agregar el número decimal actual al número formateado
            //String numeroConDecimal = numeroFormateado + numeroDecimalActual.toString();
            jLabelPantallaCalculadora.setText(numeroFormateado + " " + EURO);
        }    
    }
 
    
